var1 = 123
var2 = 'World'
print("Hello to the",var2,var1)#Usa uma , para separar as variaveis e imprimilas

print("Hello to the %s %d"%(var2,var1))#Usa a formatação de string com ajuda de %

print("Hello to the {} {}" .format(var2,var1))#Usa a formatação de string com ajuda de {}

print('Hello to the {} {}'+ var2 + str(var1))#Usa o operador + para separar variaveis na declaração da print

print(f'Hello to the{var2} {var1}')#Usa o f-string na declaração print

